package com.chinsofti.bank.test;

public class TestSwitch {

	public static void main(String[] args) {
		int x = 2;
		switch (x) {
		case 1:
			System.out.println("x=1");
			break;
		case 2:
			System.out.println("x=2");
			break;
		case 3:
			System.out.println("x=3");
			break;
		default:
			System.out.println("default");
		}

	}

}
